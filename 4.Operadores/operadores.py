# a _= 10 # Equivalente a 'a = a _ 10'
a /= 10  # Equivalente a 'a = a / 10'
a %= 10  # Equivalente a 'a = a % 10'
a **= 10  # Equivalente a 'a = a ** 10'
a //= 10  # Equivalente a 'a = a // 10'
